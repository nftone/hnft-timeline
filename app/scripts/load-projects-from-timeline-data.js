const fs = require("fs");
const path = require("path");

const loadProjectsData = () => {
    for (const project of timelineProjects) {
        createProjectDirectory(project);
        createProjectMetadataFile(project);
    }
};

const createProjectDirectory = (project) => {
    const directoryPath = path.join(
        __dirname,
        "../../data/projects",
        getDirectoryName(project)
    );
    fs.mkdirSync(directoryPath);
};

const createProjectMetadataFile = (project) => {
    const { name, slug, date, description, creator, network } = project;

    const jsonData = JSON.stringify(
        {
            name,
            slug,
            date: date.substring(0,10),
            description : description ?? "",
            creator : creator ?? "",
            links : [],
            network : network ?? ""
        },
        null,
        2
    );

    const directoryPath = path.join(
        __dirname,
        "../../data/projects",
        getDirectoryName(project),
        "data.json"
    );

    fs.writeFileSync(directoryPath, jsonData);
};

const getDirectoryName = ({ date, slug }) => `${date.substring(0,10)}-${slug}`;

